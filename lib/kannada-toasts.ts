export function getRandomToast(category: keyof typeof KANNADA_TOASTS | string, subCategory?: string): string {
    let list: string[] = [];

    if (category === 'errors') list = KANNADA_TOASTS.errors;
    else if (category === 'auth') list = KANNADA_TOASTS.auth;
    else if (category === 'validation' && subCategory) {
        list = (KANNADA_TOASTS.validation as any)[subCategory];
    }
    else if (category === 'success' && subCategory) {
        list = (KANNADA_TOASTS.success as any)[subCategory];
    }

    if (!list || list.length === 0) return "ಸಂಪರ್ಕ ಸಾಧಿಸಲು ಸಾಧ್ಯವಾಗಿಲ್ಲ";

    return list[Math.floor(Math.random() * list.length)];
}
